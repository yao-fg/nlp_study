# 定义变量
name=str(input("请输入商品名称:"))
price=float(input("请输入商品价格："))
count=int(input("请输入商品数量："))
# 计算总价
total_count=price * count
# 输出
print(f"商品名称：{name}")
print(f"单价：{price}")
print(f"数量：{count}")
print(f"总价：{total_count}")


