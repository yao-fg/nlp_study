# 1. 定义三个变量：商品名称（字符串）、商品价格（浮点数）、商品数量（整数）
# 2. 计算商品的总价（价格 × 数量）
# 3. 使用print()函数输出商品信息，格式如下：
#    "商品名称：xxx"
#    "单价：xxx元"
