# 1. 定义以下变量（初始值都是字符串）：
#    - 学号："2024001"
#    - 数学成绩："85"
#    - 语文成绩："90"
#    - 英语成绩："88"
# 2. 将成绩字符串转换为浮点数
# 3. 计算总分和平均分
# 4. 将总分和平均分转换为字符串（保留两位小数）
# 5. 输出格式化的成绩单，包括：
#    - 学号
#    - 各科成绩
#    - 总分
#    - 平均分
#
# 要求：
# - 使用int()、float()、str()进行类型转换
# - 平均分保留两位小数
# - 添加注释说明每个转换步骤


# 1. 定义以下变量（初始值都是字符串）：
num="2024001"
Math_score="85"
Chinese_score="90"
English_score="88"
# 2. 将成绩字符串转换为浮点数
Math_score=float(Math_score)
Chinese_score=float(Chinese_score)
English_score=float(Chinese_score)
# 3. 计算总分和平均分
total=Math_score+Chinese_score+English_score
average=total/3
# 4. 将总分和平均分转换为字符串（保留两位小数）
# 5. 输出格式化的成绩单，包括：
#    - 学号
#    - 各科成绩
#    - 总分
#    - 平均分
print(f"学号：{num}")
print(f"数学成绩是：{Math_score}")
print(f"语文成绩是：{Chinese_score}")
print(f"英语成绩是：{English_score}")
print(f"总成绩是：{total:.2f}")
print(f"平均成绩是：{average:.2f}")

