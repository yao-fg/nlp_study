# 1. 定义两个整数变量 a = 15, b = 4
a = 15
b = 4
# 2. 计算并输出以下运算结果：
#    - a + b（加法）
print(a+b)
#    - a - b（减法）
print(a-b)
#    - a * b（乘法）
print(a*b)
#    - a / b（除法）
print(a/b)
#    - a // b（整除）
print(a//b)
#    - a % b（取余）
print(a%b)
#    - a ** b（幂运算）
print(a**b)
# 3. 使用比较运算符比较 a 和 b 的大小关系，输出：
#    - a > b 的结果
print(f"{a}>{b} : {a>b}")
#    - a < b 的结果
print(f"{a}<{b} : {a<b}")
#    - a == b 的结果
print(f" {a}=={b} 的结果是: {a==b}")
#    - a != b 的结果
print(f" {a}!={b} 的结果是: {a!=b}")
# 4. 使用逻辑运算符，判断以下条件，并输出结果：
#    - a > 10 and b > 2
result1 = a > 10 and b > 2
print(f"a > 10 and b > 2:{result1}")
#    - a > 20 or b < 5
result2 = a > 20 or b < 5
print(f"a > 20 or b < 5 :{result2}")
#    - not (a < b)
result3=not (a < b)
print(f"not (a < b) : {result3}")

