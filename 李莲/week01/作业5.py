# 任务描述：
# 编写一个购物计算器程序，实现以下功能：
# 1. 提示用户输入以下信息：
#    - 商品1名称、单价、数量
#    - 商品2名称、单价、数量
#    - 商品3名称、单价、数量
# 2. 计算每个商品的小计（单价 × 数量）
# 3. 计算所有商品的总价（三个小计相加）
# 4. 假设折扣率为0.9（打9折），计算折扣后的价格
# 5. 输出格式化的购物清单，包括：
#    - 每个商品的名称、单价、数量、小计
#    - 总价（折扣前）
#    - 折扣率
#    - 折扣后价格
#
# 要求：
# - 使用有意义的变量名
# - 添加详细的注释
# - 输出格式要清晰，可以使用分隔线
# - 价格保留两位小数
# - 使用格式化字符串输出
#

# 1. 提示用户输入以下信息：
#    - 商品1名称、单价、数量
name1=input("请输入商品1名称：")
price1=float(input("请输入单价："))
num1=int(input("请输入数量："))
#    - 商品2名称、单价、数量
name2=input("请输入商品2名称：")
price2=float(input("请输入单价："))
num2=int(input("请输入数量："))
#    - 商品3名称、单价、数量
name3=input("请输入商品3名称：")
price3=float(input("请输入单价："))
num3=int(input("请输入数量："))
# 2. 计算每个商品的小计（单价 × 数量）
total1=price1*num1
total2=price2*num2
total3=price3*num3
# 3. 计算所有商品的总价（三个小计相加）
total=total1+total2+total3
# 4. 假设折扣率为0.9（打9折），计算折扣后的价格
rate=0.9
rate_total=total*rate
# 5. 输出格式化的购物清单，包括：
#    - 每个商品的名称、单价、数量、小计
print(f"商品1的名称为{name1},单价为{price1:.2f},数量为{num1},小计{total:.2f}")
print(f"商品2的名称为{name2},单价为{price2:.2f},数量为{num2},小计{total:.2f}")
print(f"商品3的名称为{name3},单价为{price3:.2f},数量为{num3},小计{total:.2f}")
#    - 总价（折扣前）
print(f"商品折扣前总价为{total:.2f}")
#    - 折扣率
print(f"折扣率为{rate:.2f}")
#    - 折扣后价格
print(f"商品折扣后总价为{rate_total:.2f}")