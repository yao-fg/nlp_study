# 定义变量,使用input()函数获取用户输入
name=str(input("请输入姓名:"))
age=int(input("请输入年龄："))
height=float(input("请输入身高："))

# 输出
print(f"姓名：{name}")
print(f"今年年龄：{age}")
print(f"明年年龄：{age+1}")
print(f"身高：{height}")
